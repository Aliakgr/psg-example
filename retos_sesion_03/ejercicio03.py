# Tutorial: Cómo barrer con Python

# Paso 1: Preparación
print("Paso 1: Preparación")
print("  - Encuentra una escoba adecuada. Asegúrate de que tenga cerdas limpias.")
print("  - Lava tus manos antes de comenzar.")

# Paso 2: Posición correcta
print("Paso 2: Posición correcta")
print("  - Sujeta la escoba con ambas manos, una en el mango y la otra en el extremo de las cerdas.")
print("  - Mantén la espalda recta y los pies separados para mantener el equilibrio.")

# Paso 3: Barrido
print("Paso 3: Barrido")
print("  - Comienza desde una esquina de la habitación y avanza hacia la otra.")
print("  - Barre en línea recta, empujando la suciedad hacia un montón.")
print("  - No levantes la escoba del suelo mientras barres.")

# Paso 4: Recogida de basura
print("Paso 4: Recogida de basura")
print("  - Usa una pala o una escoba pequeña para recoger la suciedad en el montón.")
print("  - Deposita la basura en una bolsa de basura o en un cubo de basura.")

# Paso 5: Limpieza final
print("Paso 5: Limpieza final")
print("  - Barre cualquier resto de suciedad hacia el montón.")
print("  - Asegúrate de que no queden partículas sueltas en el suelo.")
print("  - Lava la escoba y la pala después de usarlas.")

